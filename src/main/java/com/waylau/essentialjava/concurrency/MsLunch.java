package com.waylau.essentialjava.concurrency;

/**
 * @author <a href="http://www.waylau.com">waylau.com</a>
 * @date 2016年1月21日
 */
public class MsLunch {
	@SuppressWarnings("unused")
	private long c1 = 0;
    @SuppressWarnings("unused")
	private long c2 = 0;
    private Object lock1 = new Object();
    private Object lock2 = new Object();

    public void inc1() {
        synchronized(lock1) {
            c1++;
        }
    }

    public void inc2() {
        synchronized(lock2) {
            c2++;
        }
    }
}
